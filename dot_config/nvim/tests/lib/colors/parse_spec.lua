    T.truthy(math.abs(r[1].color.r - 1.0) < 0.05)
  end)

  T.it("parses oklch with / alpha", function()
    local r = P.parse_all("c: oklch(0.74 0.16 50 / 0.5);")
    T.eq(#r, 1)
    T.truthy(math.abs(r[1].color.a - 0.5) < 0.01)
