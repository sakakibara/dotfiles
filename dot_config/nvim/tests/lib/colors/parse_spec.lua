end)

T.describe("lib.colors.parse named", function()
  T.it("parses 'red' to #ff0000", function()
    local r = P.parse_all("color: red;")
    T.eq(#r, 1)
    T.eq(r[1].color.r, 1)
    T.eq(r[1].color.g, 0)
    T.eq(r[1].color.b, 0)
    T.eq(r[1].color.source.fmt, "named")
  end)

  T.it("parses 'darkorange' as separate token", function()
    local r = P.parse_all("color: darkorange;")
    T.eq(#r, 1)
  end)

  T.it("does NOT parse 'red' inside an identifier", function()
    local r = P.parse_all("border-radius: 4px;")
    T.eq(#r, 0)
  end)
end)

T.describe("lib.colors.parse oklab", function()
  T.it("parses oklab(0.74 0.1 0.12)", function()
    local r = P.parse_all("c: oklab(0.74 0.1 0.12);")
    T.eq(#r, 1)
    T.truthy(r[1].color.source.fmt == "oklab")
    -- Should produce a non-grey srgb color
    T.truthy(r[1].color.r ~= r[1].color.g or r[1].color.g ~= r[1].color.b,
      "expected chromatic, got grey")
  end)

  T.it("parses oklab with percent lightness", function()
    local r = P.parse_all("c: oklab(74% 0.1 0.12);")
    T.eq(#r, 1)
  end)

  T.it("parses oklab with / alpha", function()
    local r = P.parse_all("c: oklab(0.5 0.05 0.05 / 0.5);")
    T.eq(#r, 1)
    T.truthy(math.abs(r[1].color.a - 0.5) < 0.01)
  end)
end)

T.describe("lib.colors.parse tailwind classes", function()
  T.it("parses bg-red-500 as the Tailwind palette color", function()
    local r = P.parse_all([[<div class="bg-red-500">]])
    T.eq(#r, 1)
    T.truthy(r[1].color.source.tailwind_class == "red-500")
  end)

  T.it("parses bg-[#abc] arbitrary value", function()
    local r = P.parse_all([[<div class="bg-[#abc]">]])
    T.eq(#r, 1)
    T.eq(r[1].color.source.fmt, "hex")
  end)

  T.it("parses bg-[oklch(0.74_0.16_50)] arbitrary oklch", function()
    local r = P.parse_all([[<div class="bg-[oklch(0.74_0.16_50)]">]])
    T.eq(#r, 1)
    T.eq(r[1].color.source.fmt, "oklch")
  end)
end)
